# 👋 Welcome to the Band Name Generator!
# This project is a warm-up exercise to get familiar with Python's input and string handling.

# 1️⃣ Create a friendly greeting for your program.
print("Hi there and welcome to the Band Name Generator 🎸")

# 2️⃣ Ask the user for the city they grew up in.
# 📝 Hint: Use \n at the end of the input prompt to make sure the cursor appears on a new line.
city = input("Which city did you grow up in?\n")

# 3️⃣ Ask the user for the name of a pet.
pet = input("What is the name of your pet?\n")

# 4️⃣ Combine the city and pet name to make a cool band name.
print("Your band name could be: " + city + " " + pet)
